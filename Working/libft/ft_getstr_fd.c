#include "libft.h"

char	*ft_getstr_fd(const int fd)
{
	char	*s;
	size_t	i;

	s = ft_strnew(0);
	i = 0;
	while ((s[i] = ft_getchar_fd(fd)) != '\n')
	{
		i++;
	}
	s[i] = '\0';
	return (s);
}
